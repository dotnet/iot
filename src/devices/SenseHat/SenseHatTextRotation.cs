namespace Iot.Device.SenseHat
{
    /// <summary>
    /// Text rotation when rendering text, counterclockwise.
    /// </summary>
    public enum SenseHatTextRotation
    {
       /// <summary>
       /// No rotation
       /// </summary>
       Rotate_0_Degrees,

       /// <summary>
       /// Rotate by 90 degrees
       /// </summary>
       Rotate_90_Degrees,

       /// <summary>
       /// Rotate by 180 degress
       /// </summary>
       Rotate_180_Degrees,

       /// <summary>
       /// Rotate by 270 degrees
       /// </summary>
       Rotate_270_Degrees
    }
}
